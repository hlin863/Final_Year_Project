"""

A prototype file containing the basic arithmetic functions.

"""


def add(a, b):
    """Add Function"""
    return a + b


def subtract(a, b):
    """Subtract Function"""
    return a - b


def multiply(a, b):
    """Multiply Function"""
    return a * b


def divide(a, b):
    """Divide Function"""
    return a / b


def power(a, b):
    """Power Function"""
    return a ** b
